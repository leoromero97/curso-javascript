let puntaje1 = 5;
console.log(puntaje1);
// Incrementar
console.log(
  "Incrementar de a uno, primero ejecuta el valor inicial y luego incrementa",
  puntaje1++
);
console.log(
  "Incrementar de a uno, primero incrementa y luego muestra el valor",
  ++puntaje1
);

// Decrementar
console.log(
  "Decrementar de a uno, primero ejecuta el valor inicial y luego incrementa",
  puntaje1--
);
console.log(
  "Decrementar de a uno, primero incrementa y luego muestra el valor",
  --puntaje1
);


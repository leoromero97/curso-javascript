const number1 = 20;
const number2 = 30;
console.log(number1);
console.log(number2);

// número flotante
const number3 = 20.2;
console.log(number3);
const number4 = 0.12312131;
console.log(number4);

// número negativo
const number5 = -10;
console.log(number5);

// Otra forma de crear un número
const number6 = Number(80);
console.log(number6);
// Otra forma de crear un objeto de número
const number7 = new Number(70);
console.log(number7);

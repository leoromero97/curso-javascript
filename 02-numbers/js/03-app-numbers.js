// El objeto Math
console.log(Math);
// El resultado de PI
console.log(Math.PI);
// Redondear números
console.log("Redondear", Math.round(2.4));
console.log("Redondear hacia arriba", Math.ceil(2.2));
console.log("Redondear hacia abajo", Math.floor(2.7));

// Operaciones complejas
console.log("Raíz cuadrada", Math.sqrt(144));
console.log("Valor absoluto", Math.abs(500));
console.log("Potencia", Math.pow(2, 4));

// Operaciones del día a día
console.log("Conocer el número mínimo", Math.min(4, 7, -1, 12));
console.log("Conocer el número máximo", Math.max(4, 7, 1, 12));
console.log("Número aleatorio entre 0 y 100", Math.floor(Math.random() * 100));
